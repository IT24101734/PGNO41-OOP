package com.movierental.model.movie;

public class Movie {
    // Default constructor
    public Movie() {
        this.movieId = "";
        this.title = "";
        this.director = "";
        this.genre = "";
        this.releaseYear = 0;
        this.rating = 0.0;
        this.available = true;
        this.coverPhotoPath = "";
    }
}


